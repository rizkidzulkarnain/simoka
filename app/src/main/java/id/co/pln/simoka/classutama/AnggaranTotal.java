public class AnggaranTotal {

    private String skki;
    private String pagu_anggaran;
    private String persentase;

    public AnggaranTotal() {
    }

    public String getSkki() {
        return skki;
    }

    public void setSkki(String skki) {
        this.skki = skki;
    }

    public String getPagu_anggaran() {
        return pagu_anggaran;
    }

    public void setPagu_anggaran(String pagu_anggaran) {
        this.pagu_anggaran = pagu_anggaran;
    }

    public String getPersentase() {
        return persentase;
    }

    public void setPersentase(String persentase) {
        this.persentase = persentase;
    }
}
